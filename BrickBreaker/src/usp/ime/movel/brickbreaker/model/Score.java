package usp.ime.movel.brickbreaker.model;

public class Score {
	private Long id = null;
	private Long score = null;

	public Score() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getScore() {
		return score;
	}

	public void setScore(Long score) {
		this.score = score;
	}
}